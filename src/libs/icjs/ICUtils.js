var ICUtils = exports.ICUtils = function ICUtils() { };

// define isArray
ICUtils.isArray = Array.isArray || function (x) {
  return Object.prototype.toString.call(x) === '[object Array]';
};

// define isArrayBuffer
ICUtils.isArrayBuffer = function (x) {
  return typeof ArrayBuffer !== 'undefined' && x instanceof ArrayBuffer;
};

// define isArrayBufferView
ICUtils.isArrayBufferView = function (x) {
  return x && ICUtils.isArrayBuffer(x.buffer) && x.byteLength !== undefined;
};

